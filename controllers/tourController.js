const multer = require('multer');
const sharp = require('sharp');
const Tour = require('../models/tourModel');
const catchAsync = require('../utils/catchAsync');
const factory = require('./handlerFactory');
const AppError = require('../utils/appError');

//Multer file memory to store the image as a buffer
const multerStorage = multer.memoryStorage();

//Multer file filter config
const multerFilter = (req, file, callbackFN) => {
  if (
    file.mimetype.split('/')[0] === 'image' ||
    file.mimetype.startsWith('image')
  ) {
    callbackFN(null, true);
  } else {
    callbackFN(
      new AppError(
        'File you uploaded is not an image! Please upload only images',
        400
      ),
      false
    );
  }
};

//multer function
const upload = multer({
  storage: multerStorage,
  fileFilter: multerFilter,
});

//if(one image) => upload.single('image')
//if(5 images) => upload.array('images', 5)
//if(mixed like below) => upload.fields({object for eachfield}, {object for each field})

//middleware created from multer function to be called in the Router
exports.uploadTourImages = upload.fields([
  { name: 'imageCover', maxCount: 1 },
  { name: 'images', maxCount: 3 },
]);

exports.resizeTourImages = catchAsync(async (req, res, next) => {
  if (!req.files.imageCover || !req.files.images) return next();

  //1) CoverImage

  const imageCoverFileName = `tour-${req.params.id}-${Date.now()}-cover.jpeg`;

  await sharp(req.files.imageCover[0].buffer)
    .resize(2000, 1333) //3:2 image ratio
    .toFormat('jpeg')
    .jpeg({ quality: 90 })
    .toFile(`public/img/tours/${imageCoverFileName}`);

  req.body.imageCover = imageCoverFileName;

  //2) Images Loop
  req.body.images = [];

  await Promise.all(
    req.files.images.map(async (file, i) => {
      const fileName = `tour-${req.params.id}-${Date.now()}-${i + 1}.jpeg`;

      await sharp(file.buffer)
        .resize(2000, 1333) //3:2 image ratio
        .toFormat('jpeg')
        .jpeg({ quality: 90 })
        .toFile(`public/img/tours/${fileName}`);

      req.body.images.push(fileName);
    })
  );
  // console.log(req.body);
  next();
});

